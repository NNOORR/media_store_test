<template>
    <div class="container">
        <div class="row justify-content-center">
            <div class="col-md-8">
                <div class="card">
                    <div class="card-header">Login</div>

                    <div class="card-body">
                        <form @submit.prevent="login" class="mb-3">

                            <div class="form-group row">
                                <label for="email" class="col-md-4 col-form-label text-md-right">Email</label>

                                <div class="col-md-6">
                                    <input id="email" type="email" class="form-control" name="email" value="" required autocomplete="email" autofocus v-model="user.email">

                                </div>
                            </div>

                            <div class="form-group row">
                                <label for="password" class="col-md-4 col-form-label text-md-right">Password</label>

                                <div class="col-md-6">
                                    <input id="password" type="password" class="form-control" name="password" required autocomplete="current-password" v-model="user.password">
                                </div>
                            </div>

                            <div class="form-group row">
                                <div class="col-md-6 offset-md-4">
                                    <div class="form-check">
                                        <input class="form-check-input" type="checkbox" name="remember" id="remember" >

                                        <label class="form-check-label" for="remember">
                                            Remember Me
                                        </label>
                                    </div>
                                </div>
                            </div>

                            <div class="form-group row mb-0">
                                <div class="col-md-8 offset-md-4">
                                    <button type="submit" class="btn btn-primary">
                                       Button
                                    </button>

                                </div>
                            </div>
                        </form>
                    </div>
                </div>
            </div>
        </div>
    </div>

</template>

<script>
    export default {
        data() {
            return {
                user: {
                    email: '',
                    password: ''
                }
            }
        },

        methods: {
            login(){
               //Try to login
               fetch('api/auth/login', {
                   method: 'post',
                   body: JSON.stringify(this.user),
                   headers:{
                       'content-type': 'application/json'
                   }
               })
                   .then(res => res.json())
                   .then(data => {
                       if(data.status == 1) {
                           window.localStorage.setItem('token', data.objList['token']);
                           this.$router.push('/');
                       }
                       else
                       {
                           alert('Invalid email or password, please try again!');
                       }
                   }).catch(err => console.log(err));
            }
        }
    }
</script>